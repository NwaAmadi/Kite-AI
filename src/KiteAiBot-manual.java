    // Function to check if the pixel matches the target color
    private static boolean pixelMatches(Robot robot, int x, int y, Color targetColor) {
        BufferedImage screenshot = robot.createScreenCapture(new Rectangle(x, y, 1, 1));
        Color pixelColor = new Color(screenshot.getRGB(0, 0));
        System.out.println(pixelColor.getRed()+","+pixelColor.getGreen()+","+pixelColor.getBlue());
        return pixelColor.equals(targetColor);
    }

    // Function to wait for a specific pixel color
    private static void waitForPixel(Robot robot) {
        System.out.println("Waiting for the target pixel color...");

        while (true) {
            if (pixelMatches(robot, TARGET_X, TARGET_Y, TARGET_COLOR)) {
                System.out.println("Target color detected! Resuming typing...");
                break;
            }
            try {
                Thread.sleep(1000); // Check every second
            } catch (InterruptedException e) {
                e.printStackTrace();
            }
        }
    }

    // Function to simulate typing a string
    private static void typeString(Robot robot, String text) {
        try{
            for (char c : text.toCharArray()) {
                int keyCode = KeyEvent.getExtendedKeyCodeForChar(c);
                if (KeyEvent.CHAR_UNDEFINED == keyCode) {
                    continue;
                }
                robot.keyPress(keyCode);
                robot.delay(50);
                robot.keyRelease(keyCode);
            }
        }
        catch(Exception e){
            System.out.println(e.getMessage());
        }
        
    }

